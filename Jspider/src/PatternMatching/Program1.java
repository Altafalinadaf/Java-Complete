package PatternMatching;

public class Program1 {
	public static void main(String[] args)  {
		for(int row=1;row<=5;row++) {
			/*
			 * if(row>=col){
			 * System.out.print("*");
			 * else{
			 * System.out.print(" ");
			 */
			for(int col=1;col<=row;col++) {
				System.out.print("* ");
				
			}
			System.out.println();
		}
		
	}
	

}
