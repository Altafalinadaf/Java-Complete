package ForLoop_Programs;

public class Horizontal_printing {
	public static void main(String[] args) {
		for(int i=1;i<=5;i++) {
			System.out.print(i+" ");
		}
	    System.out.println();
	    System.out.println("REVERSE PRINTING");
		for(int i=5;i>=1;i--) {
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.println("REVERSE PRINTING");
		for(int i=6;i<=12;i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.println("REVERSE PRINTING");
		for(int i=21;i<=25;i++) {
			System.out.print(i+" ");
		}
	}

}
