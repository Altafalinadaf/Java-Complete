package looparray1;

public class while_loop_printing {
	public static void main(String[] args) {
		int n=1;
		while(n<=25) {
		    System.out.println(n);
		    n++;
		}
		System.out.println("Number 30 to 60");
		int i=30;
		while(i<=60) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("Numeber 61 to 31");
		int j=61;
		while(j>=31) {
			System.out.println(j);
			j--;
		}
		System.out.println("91 to 5");
		int k=91;
		while(k>=5) {
			System.out.println(k);
			k--;
		}
		
		System.out.println("Horizontal Priting Number ");
		int n1=1;
		while(n1<=10) {
			System.out.print(n1+" ");
			n1++;
		}
		System.out.println();
		System.out.println("10 to 1");
		int n2=10;
		while(n2>=1) {
			System.out.print(n2+" ");
			n2--;
		}
		
		System.out.println();
		System.out.println("36 to 76");
		int n3=36;
		while(n3<=76) {
			System.out.print(n3+" ");
			n3++;
		}
		
		System.out.println();
		System.out.println("81 to 41");
		int n4=81;
		while(n4>=41) {
			System.out.print(n4+" ");
			n4--;
		}
	}
}
