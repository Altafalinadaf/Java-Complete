public class variables {
	public static void main(String args[]) {
		// example of integer variables
		int a; // declaration 
		a=1; // initialization 
		System.out.println(a);
		
		byte a1=127; // last range of byte is 127
		System.out.println(a1);
		
		int b=6; // declaration & initialization 
		System.out.println(b);
		
		
		// examples of long same as integer
		long c=7; // declaration & initialization
		System.out.println(c);
		
		
		// used for decimal point
		// example of 1.float it contain f at the last like 7.00f
		float d=7.234f;
		System.out.println(d);
		
		
		// example of 2.double it contain decimal point without putting f to last
		double e=8.55553;
		System.out.println(e);
		
		// example of character 
		char f='z'; // declaration & initialization of char
		System.out.println(f);
		
		char g='5'; // declaration & initialization of char
		System.out.println(g);
		
		
		// BOolean values
		
		boolean h=true; // true value
		System.out.println(h);
		
		boolean i=false; // false value
		System.out.println(i);
		
		// asking the string input
		String s="ali";
		System.out.println("name : "+s);
//		Scanner scanner=new Scanner(System.in);
//		s=scanner.nextLine();
//		System.out.println(s);
//		scanner.close();
	}
}
